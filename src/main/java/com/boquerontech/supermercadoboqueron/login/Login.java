    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // Creamos una cadena de texto para coger los datos del tfUser
        String user = tfUser.getText();                                       
        // Creamos una cadena de texto para coger los datos del tfPassword
        String password = String.valueOf(tfPassword.getPassword());
        // Si el contenido es igual a Usuario y la contraseña a 123 hara lo siguiente
        if (user.equals("Torrente") && password.equals("123")) {
            // Llamamos al Objeto Inicio y le damos los parametros user y password
            Inicio inicio = new Inicio(user, password);
            // Le decimos que se abra la ventana
            inicio.setVisible(true);
            // Cerramos la ventana de Login
            this.dispose(); 
        // En el caso de que el usuario / contraseña esten vacios o tengan los siguientes datos que haga lo siguiente
        } else if (user.isBlank() || password.isBlank() || user.equals("Introduzca nombre de usuario") || password.equals("Introduzca una contraseña"))  {
            // Mostrar el panel con el contenido introducizo y WARNING_MESSAGE
            JOptionPane.showMessageDialog(
                this,
                "Por favor, introduce unas credenciales para acceder a la aplicación",
                "Credenciales vacías",
                JOptionPane.WARNING_MESSAGE
            );
            // Si no
        } else {
            // Mostrar el panel con el contenido introducizo y ERROR_MESSAGE
            JOptionPane.showMessageDialog(
                this,
                "El usuario introducido no existe o la contraseña es incorrecta",
                "Credenciales incorrectas",
                JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_btnLoginActionPerformed

