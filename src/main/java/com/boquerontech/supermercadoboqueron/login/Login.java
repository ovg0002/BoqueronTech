    private void tfUserFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_tfUserFocusGained
        // Cuando se hace click en tfUser si tiene el texto ese se vacia
        if (tfUser.getText().equals("Introduzca nombre de usuario")) {
            tfUser.setText("");
            // Se le da color negro para cuando escriban
            tfUser.setForeground(Color.BLACK);
        }
    }//GEN-LAST:event_tfUserFocusGained

    private void tfPasswordFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_tfPasswordFocusGained
        // Se crea un String en password para que coja la contraseña
        String currentPassword = new String(tfPassword.getPassword());
        // Si contraseña es igual a esa que se borre el contenido al hacer click
        if ("Introduzca una contraseña".equals(currentPassword)) {
            tfPassword.setText("");
            tfPassword.setForeground(Color.BLACK);
             // Ahora sí mostrar los puntos
            tfPassword.setEchoChar('•');
        }
    }//GEN-LAST:event_tfPasswordFocusGained

    private void tfUserFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_tfUserFocusLost
       // Cuando pierde el foco, si está vacío, pone el placeholder
        if (tfUser.getText().trim().isEmpty()) {
            tfUser.setText("Introduzca nombre de usuario");
            tfUser.setForeground(Color.GRAY); // Placeholder en gris
        }
    }//GEN-LAST:event_tfUserFocusLost

    private void tfPasswordFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_tfPasswordFocusLost
        if (tfPassword.getPassword().length == 0) {
            tfPassword.setText("Introduzca una contraseña");
            tfPassword.setForeground(Color.GRAY);
            tfPassword.setEchoChar((char)0); // Texto visible otra vez
        }
    }//GEN-LAST:event_tfPasswordFocusLost

